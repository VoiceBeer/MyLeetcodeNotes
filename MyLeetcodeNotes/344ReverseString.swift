/*
 Write a function that takes a string as input and returns the string reversed.
 
 Example:
 Given s = "hello", return "olleh".
 */

/// 没啥说的
/// Nothing to say, fxxking ez

class Solution_344 {
    func reverseString(_ s: String) -> String {
        return String(s.reversed())
    }
}

